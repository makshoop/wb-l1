// Элемент DOM, в который будет выводиться размер коллстэка
const valueElement = document.querySelector('.page-value');

// Переменная для хранения размера коллстэка
let stackSize = 0;

// Функция для рекурсивных вызовов, увеличивающих размер стека
function getStackSize() {
    try {
        stackSize++;
        getStackSize();
    } catch (error) {
        // Перехватываем ошибку переполнения стека
        return stackSize;
    }
}

// Получаем размер стека вызовов
const stackLimit = getStackSize();

// Выводим размер стека в элемент на странице
valueElement.textContent = stackSize;

// Выводим размер стека в консоль для дополнительного анализа
console.log('Размер коллстэка:', stackSize);

// Оценочные значения размера коллстэка в различных браузерах
// (реальные значения могут варьироваться в зависимости от браузера, версии и окружения)

